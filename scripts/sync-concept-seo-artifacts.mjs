#!/usr/bin/env node

import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { resolveActiveSiteRuntime } from '../apps/blog/site-profiles.mjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');
const { concept } = resolveActiveSiteRuntime(process.env);

const artifacts = [
  { source: path.join(repoRoot, 'apps/blog/dist/rss.xml'), exportName: 'conceptRssXml' },
  { source: path.join(repoRoot, 'apps/blog/dist/sitemap.xml'), exportName: 'conceptSitemapXml' },
  { source: path.join(repoRoot, 'apps/blog/dist/robots.txt'), exportName: 'conceptRobotsTxt' },
];

const outputDir = path.join(
  repoRoot,
  'apps/cloudflare/workers',
  concept.cloudflare.routeWorkerDirectory,
  'src/generated',
);
const outputFile = path.join(outputDir, 'seo-artifacts.ts');

async function main() {
  const entries = await Promise.all(
    artifacts.map(async ({ source, exportName }) => ({
      exportName,
      content: await readFile(source, 'utf8'),
    })),
  );

  await mkdir(outputDir, { recursive: true });

  const body =
    `// Generated by scripts/sync-concept-seo-artifacts.mjs\n` +
    `// Active concept: ${concept.key}\n` +
    `// Source of truth: apps/blog/dist/rss.xml, sitemap.xml, robots.txt\n\n` +
    entries.map(({ exportName, content }) => `export const ${exportName} = ${JSON.stringify(content)};\n`).join('');

  await writeFile(outputFile, body, 'utf8');
  console.log(`Synced ${concept.key} SEO artifacts to ${outputFile}`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
});
