#!/usr/bin/env node

import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');

const artifacts = [
  { source: path.join(repoRoot, 'apps/blog/dist/rss.xml'), exportName: 'guideRssXml' },
  { source: path.join(repoRoot, 'apps/blog/dist/sitemap.xml'), exportName: 'guideSitemapXml' },
  { source: path.join(repoRoot, 'apps/blog/dist/robots.txt'), exportName: 'guideRobotsTxt' },
];

const outputDir = path.join(repoRoot, 'apps/cloudflare/workers/guide-proxy/src/generated');
const outputFile = path.join(outputDir, 'seo-artifacts.ts');

async function main() {
  const entries = await Promise.all(
    artifacts.map(async ({ source, exportName }) => ({
      exportName,
      content: await readFile(source, 'utf8'),
    })),
  );

  await mkdir(outputDir, { recursive: true });

  const body =
    `// Generated by scripts/sync-guide-seo-artifacts.mjs\n` +
    `// Source of truth: apps/blog/dist/rss.xml, sitemap.xml, robots.txt\n\n` +
    entries.map(({ exportName, content }) => `export const ${exportName} = ${JSON.stringify(content)};\n`).join('');

  await writeFile(outputFile, body, 'utf8');
  console.log(`Synced guide SEO artifacts to ${outputFile}`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
});
